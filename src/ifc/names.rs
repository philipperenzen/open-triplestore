/// Spatial-structure entity → BOT class local name.
pub const SPATIAL_BOT: &[(&str, &str)] = &[
    ("IFCSITE", "Site"),
    ("IFCBUILDING", "Building"),
    ("IFCBUILDINGSTOREY", "Storey"),
    ("IFCSPACE", "Space"),
];

pub fn camel(entity_upper: &str) -> &str {
    CAMEL
        .iter()
        .find(|c| c.eq_ignore_ascii_case(entity_upper))
        .copied()
        .unwrap_or(entity_upper)
}

pub fn attrs_of(entity_upper: &str) -> Option<&'static [&'static str]> {
    ATTRS
        .iter()
        .find(|(e, _)| *e == entity_upper)
        .map(|(_, a)| *a)
}
