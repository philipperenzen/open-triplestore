#!/usr/bin/env python3
"""Build the vendored prefix dataset for the internal prefix service.

Merges two sources into src/prefixes/data/prefixes-snapshot.json:

1. The full prefix.cc dump (``/popular/all.file.json``) — the de-facto
   community registry (~3.5k prefixes, ordered by access count, one
   vote-winning namespace per prefix).  The prefix.cc codebase is released
   under The Unlicense; the mapping data is community-contributed factual
   data that prefix.cc itself distributes for bulk reuse.
2. The LOV catalog generated by build_lov_catalog.py — contributes
   ``vann:preferredNamespacePrefix`` pairs for vocabularies prefix.cc does
   not know (LOV data is CC BY 4.0).

Entries are validated against the same constraints the backend enforces
(`[A-Za-z][A-Za-z0-9_-]*` labels ≤ 64 chars, http/https namespaces) so the
Rust loader never has to drop records silently.  On label conflict the
prefix.cc mapping wins (community-voted); the LOV entry is skipped.

Usage:
    python scripts/build_prefix_dataset.py <prefixcc-all.json> <lov-catalog.json.gz> \
        <out.json> [--prefixcc-snapshot-date YYYY-MM-DD]
"""

from __future__ import annotations

import argparse
import gzip
import json
import re
import sys

LABEL_RE = re.compile(r"^[A-Za-z][A-Za-z0-9_-]{0,63}$")


def valid_namespace(ns: str) -> bool:
    return isinstance(ns, str) and ns.startswith(("http://", "https://")) and len(ns) <= 500


def main() -> int:
    ap = argparse.ArgumentParser()
    ap.add_argument("prefixcc")
    ap.add_argument("lov_catalog")
    ap.add_argument("out")
    ap.add_argument("--prefixcc-snapshot-date", default="unknown")
    args = ap.parse_args()

    # dict order == prefix.cc popularity order (dump is sorted by access count)
    pcc = json.load(open(args.prefixcc, encoding="utf-8"))
    lov = json.load(gzip.open(args.lov_catalog))

    entries = []
    seen = {}
    dropped = 0
    for rank, (label, ns) in enumerate(pcc.items(), start=1):
        if not LABEL_RE.match(label) or not valid_namespace(ns):
            dropped += 1
            continue
        seen[label] = ns
        entries.append({"prefix": label, "namespace": ns, "rank": rank, "source": "prefix.cc"})

    lov_added = 0
    for vocab in lov["vocabularies"]:
        label, ns = vocab["prefix"], vocab["nsp"]
        if label in seen or not LABEL_RE.match(label) or not valid_namespace(ns):
            continue
        lov_added += 1
        entries.append(
            {
                "prefix": label,
                "namespace": ns,
                "rank": len(pcc) + lov_added,
                "source": "lov",
            }
        )

    out = {
        "format_version": 1,
        "sources": {
            "prefix.cc": {
                "url": "https://prefix.cc/popular/all.file.json",
                "snapshot_date": args.prefixcc_snapshot_date,
                "note": "Community prefix registry; repository released under The Unlicense.",
            },
            "lov": {
                "url": lov["source"]["url"],
                "snapshot_date": lov["source"]["snapshot_date"],
                "license": "CC BY 4.0",
                "attribution": lov["source"]["attribution"],
            },
        },
        "prefixes": entries,
    }
    json.dump(out, open(args.out, "w", encoding="utf-8"), ensure_ascii=False, indent=1)
    print(f"prefix.cc entries: {len(pcc)} (dropped {dropped} invalid), lov additions: {lov_added}")
    print(f"total: {len(entries)}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
